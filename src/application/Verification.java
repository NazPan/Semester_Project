package application;

public class Verification {
	private int tableno;
	private int index;
	
	public Verification() {
		tableno=0;
		index=0;
	}
	public int getTableno() {
		return tableno;
	}
	public void setTableno(int tableno) {
		this.tableno = tableno;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	
}
